     */
    public function index($title = '')
    {
        $data['user'] = auth()->user();
        $data['title'] = $title;

        switch (auth()->user()->role) {
            case 0 : $data['role'] = 'Пользователь'; break;
            case 1 : $data['role'] = 'Менеджер'; break;
            case 2:  $data['role'] = 'Администратор'; break;
            case 3:  $data['role'] = 'Супер-администратор'; break;
        }

        return view('layouts.header', $data);
    }

